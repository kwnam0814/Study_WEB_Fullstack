// 변수 선언
let box;
box = 30;
console.log(box); // 30
console.log(typeof box); // number

box = 20;
console.log(box); // 20
console.log(typeof box); // number

// 문자형 자료 string
let myName = "홍길동";
console.log(myName); // 홍길동
console.log(typeof myName); // string

let yourName = '김철수';
console.log(yourName); // 김철수
console.log(typeof yourName); // string

// boolean형 자료
let isCompare = 10 > 100;
console.log(isCompare); // false
console.log(typeof isCompare); // boolean

let isFine = 10 < 100;
console.log(isFine); // true
console.log(typeof isFine); // boolean